package com.p1;

public class Stringconcept {

	public static void main(String[] args) {
		String a="suruthi";
		String b="suruthi";
		System.out.println("Equals="+a.equals(b));
		System.out.println("Equals Ignore case="+a.equalsIgnoreCase(b));
		System.out.println("length="+a.length());
		System.out.println("charAt="+a.charAt(0));
		System.out.println("uppercase="+a.toUpperCase());
		System.out.println("lowercase="+a.toLowerCase());
		System.out.println("Replace="+a.replace("Priya","sivakumar"));
		System.out.println("contains="+a.contains("Priya"));
		System.out.println("starts with="+a.startsWith("Sahana"));
		System.out.println("Ends with="+a.endsWith("hi"));
		
		
		// TODO Auto-generated method stub

	}

}